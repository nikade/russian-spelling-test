#!/bin/bash
# Генерация аудио для оставшихся слов

words=(
    "милая девочка;девочка"
    "дежурный ученик;дежурный"
    "цветной карандаш;карандаш"
    "рыжая лисица;лисица"
    "ехать на машине;машина"
    "столица Москва;Москва"
    "любимая Родина;Родина"
    "наша великая Россия;Россия"
    "учитель учитель;учитель"
    "очень хорошо;хорошо"
    "владеть языком;язык"
)

for item in "${words[@]}"; do
    IFS=';' read -r phrase word <<< "$item"

    text="-sil<[400]> ${phrase} sil<[400]>${word}"

    printf '%s' "$text" | bash tts.sh > "audio/ru/${word}.mp3"

    echo "Сгенерировано: ${word}.mp3"
    sleep 0.2
done

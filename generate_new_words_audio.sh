#!/bin/bash
# Генерация аудио для новых словарных слов 2 класса

words=(
    "сегодня весело;весело"
    "шустрый воробей;воробей"
    "сказочная деревня;деревня"
    "серый заяц;заяц"
    "здравствуй друг;здравствуй"
    "пустой класс;класс"
    "пятнистая корова;корова"
    "бурый медведь;медведь"
    "ученик второго класса;ученик"
    "ученица второго класса;ученица"
    "русский язык;русский"
)

for item in "${words[@]}"; do
    IFS=';' read -r phrase word <<< "$item"

    # Формируем текст с паузами (начальное тире обязательно!)
    text="-sil<[400]> ${phrase} sil<[400]>${word}"

    # Генерируем через tts.sh
    printf '%s' "$text" | bash tts.sh > "audio/ru/${word}.mp3"

    echo "Сгенерировано: ${word}.mp3"
    sleep 0.2
done
